package tags;

public class Tag {
	private String tag;
	
	/**
	 * @return the tag
	 */
	public String getTag() {
		return tag;
	}

	/**
	 * @param tag the tag to set
	 */
	public void setTag(String tag) {
		this.tag = tag;
	}

	public Tag(String t){
		tag = t;
	}
	
	public String toString(){
		return tag;
	}
}
